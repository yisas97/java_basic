public class SwitchStatement
{

    public static void main(String[] args)
    {
        String a = "m";
        switch (a)
        {
            case "a":
                System.out.println("Soy la letra a");
                break;
            case "m":
                System.out.println("Soy la letra m");
                break;
            default:
                System.out.println("Soy una letra rara");
        }
    }
}
